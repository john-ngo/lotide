const without = function(source, itemsToRemove) {
  const array = [];

  for (const item of source) {
    if (!itemsToRemove.includes(item)) {
      array.push(item);
    }
  }

  return array;
};

console.log(without([1, 2, 3], [1]));
console.log(without(["1", "2", "3"], [1, 2, "3"]));

